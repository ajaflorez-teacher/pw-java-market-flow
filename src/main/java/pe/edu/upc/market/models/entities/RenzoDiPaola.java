package pe.edu.upc.market.models.entities;

public class RenzoDiPaola {
	private Float id;
	private String RenzoDiPaola;
	private String nombre;
	private String apellido;

	public Float getId() {
		return id;
	}

	public void setId(Float id) {
		this.id = id;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellido() {
		return apellido;
	}

	public void setApellido(String apellido) {
		this.apellido = apellido;
	}

	public String getRenzoDiPaola() {
		return RenzoDiPaola;
	}

	public void setRenzoDiPaola(String renzoDiPaola) {
		RenzoDiPaola = renzoDiPaola;
	}
	
}
